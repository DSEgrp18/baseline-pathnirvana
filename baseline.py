import pandas as pd, os, glob, random
random.seed(1234)

SPEAKER   = 'mettananda'   # 'mettananda' (male, ~5400 clips) or 'oshadi' (female, ~985)
TEXT_MODE = 'sinhala'      # 'sinhala' or 'roman'

# metadata.csv: file_id | romanized | sinhala | speaker  (pipe-delimited, no header)
rows = []
with open(f'{ROOT}/metadata.csv', encoding='utf-8') as f:
    for line in f:
        p = line.rstrip('\n').split('|')
        if len(p) == 4:
            rows.append(p)
df = pd.DataFrame(rows, columns=['id', 'roman', 'sinhala', 'speaker'])
df = df[df.speaker == SPEAKER].copy()

def clean(t):
    t = t.strip()
    t = t.replace('-පෙ-', '').strip()   # drop the '-පෙ-' repetition marker
    return ' '.join(t.split())

col = 'sinhala' if TEXT_MODE == 'sinhala' else 'roman'
df['text'] = df[col].map(clean)

# keep only rows whose wav actually exists
df['wav'] = df.id.map(lambda i: os.path.join(WAVS_DIR, i + '.wav'))
df = df[df.wav.map(os.path.exists) & (df.text.str.len() > 0)].reset_index(drop=True)
print('usable clips:', len(df))


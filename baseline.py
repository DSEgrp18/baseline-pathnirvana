audio = VitsAudioConfig(sample_rate=22050, win_length=1024, hop_length=256,
                        num_mels=80, mel_fmin=0, mel_fmax=None)

# --- vocab from the metadata file (restart-proof; no df dependency) ---
punctuation_str = "!'(),-.:;?‘’“” "
texts = []
with open(f'{DATA}/metadata_train.csv', encoding='utf-8') as f:
    for line in f:
        parts = line.rstrip('\n').split('|')
        if len(parts) >= 2:
            texts.append(parts[1])                 # column 1 = text
letter_chars = ''.join(sorted(set(''.join(texts)) - set(punctuation_str)))
print('chars:', len(letter_chars), '| sample:', letter_chars[:30])

chars_config = CharactersConfig(
    characters_class='TTS.tts.utils.text.characters.Graphemes',
    pad='<PAD>', eos='<EOS>', bos='<BOS>', blank='<BLNK>',
    characters=letter_chars,
    punctuations=punctuation_str,
)

config = VitsConfig(
    audio=audio,
    run_name='vits_sinhala',
    batch_size=16,
    eval_batch_size=8,
    num_loader_workers=2,
    num_eval_loader_workers=2,
    run_eval=True,
    test_delay_epochs=5,
    epochs=50,
    use_phonemes=False,
    characters=chars_config,
    text_cleaner='multilingual_cleaners',
    print_step=50,
    save_step=1000,
    save_n_checkpoints=3,
    mixed_precision=True,
    output_path=OUT,
    datasets=[train_ds],
    test_sentences=[
        'අද දිනය හොඳ දිනයකි.',
        'ශ්‍රී ලංකාව ලස්සන රටකි.',
    ],
)


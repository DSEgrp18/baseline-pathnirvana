# -------- torchaudio.info fallback patch (harmless if unused) --------
import torchaudio
from TTS.tts.datasets import dataset
def patched_get_audio_size(audiopath):
    try:
        return torchaudio.info(audiopath).num_frames
    except Exception:
        import librosa
        y, sr = librosa.load(audiopath, sr=None)
        return len(y)
dataset.get_audio_size = patched_get_audio_size
